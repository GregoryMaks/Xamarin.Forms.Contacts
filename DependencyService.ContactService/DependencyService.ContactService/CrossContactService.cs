using System;

namespace Plugin.ContactService
{
    /// <summary>
    /// Cross ContactService
    /// </summary>
    public static class CrossContactService
    {
        static Lazy<IContactService> implementation = new Lazy<IContactService>(
            () => CreateContactService(),
            System.Threading.LazyThreadSafetyMode.PublicationOnly
        );

        /// <summary>
        /// Gets if the plugin is supported on the current platform.
        /// </summary>
        public static bool IsSupported => implementation.Value == null ? false : true;

        /// <summary>
        /// Current plugin implementation to use
        /// </summary>
        public static IContactService Current
        {
            get
            {
                IContactService ret = implementation.Value;
                if (ret == null)
                    throw NotImplementedInReferenceAssembly();
                return ret;
            }
        }

        static IContactService CreateContactService()
        {
#if NETSTANDARD1_0 || NETSTANDARD2_0
            return null;
#else
            return new ContactService();
#endif
        }

        internal static Exception NotImplementedInReferenceAssembly() =>
            new NotImplementedException("This functionality is not implemented in the portable version of this assembly.  You should reference the NuGet package from your main application project in order to reference the platform-specific implementation.");

    }
}
